#include <stdio.h>

int main(){
    int a = 4, b = 5;
    printf("a is less then b %d\n",(a<b));
    printf("a is greater then b %d\n",(a>b));
    printf("a is less then or equal to b %d\n",(a<=b));
    printf("a is greater then or equal to b %d\n",(a>=b));
    printf("a is equal to b %d\n",(a==b));
    printf("a is not equal to b %d\n",(a!=b));

    return 0;
}